#include<iostream>
using namespace std;

int main()
{
    int a;
    float b;
    char c;
    double d;

    cout<<"Size of int: "<<sizeof(a)<<" bytes"<<endl;
    cout<<"Size of float: "<<sizeof(b)<<" bytes"<<endl;
    cout<<"Size of char: "<<sizeof(c)<<" bytes"<<endl;
    cout<<"Size of double: "<<sizeof(d)<<" bytes"<<endl;
}
